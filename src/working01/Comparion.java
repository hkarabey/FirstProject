package working01;

public class Comparion {
    public static void main(String[] args) {
        /*
        >   great
        <   less
        >=  greater or equal
        <=  less or equal
        ==  equal
        !=  not equal
         */

        int a = 200;
        int b = 100;

        boolean result1 = (a > b);
        boolean result2 = (a <= b);
        boolean result3 = (a != b);

        System.out.println(result1);
        System.out.println(result2);
        System.out.println(result3);


    }


}
