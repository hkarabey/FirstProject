package working01;

public class LogicalOperators {
    /*
       &&      and
       ||      or
       !       not
       results is true or false

     */
    public static void main(String[] args) {
        boolean x=true;
        boolean y=false;
        boolean a,b,c;
        a = x&&y;
        b = x||y;
        c = !x;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);


    }


}
