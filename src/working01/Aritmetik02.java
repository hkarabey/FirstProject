package working01;

public class Aritmetik02 {
    public static void main(String[] args) {
        int a = 12;
        int b = 3;


        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a % b);


        int decre = ++a;
        int dec2 = a++;


    /*
        ++a ise once ılk a  yı bir artır sonra yeni
        tanımla demek

        a++ ise once ayı yaz sonra yeni
        a yı bır artırarak tanımla
        bir sonraki işleme oyle devaöm et
         */
        System.out.println(decre);
        System.out.println(dec2);


    }


}
